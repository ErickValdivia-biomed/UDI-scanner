package io.github.erickvaldivia.udiscanner.parser.ifa

/**
 * Constants used by the IFA parser.
 */
internal object IFAConstants {

    /**
     * ANSI MH10 Device Identifier.
     */
    const val DEVICE_IDENTIFIER = "9N"

    /**
     * PPN PRA code.
     */
    const val PPN = "11"

    /**
     * HPC PRA code.
     */
    const val HPC = "13"

    /**
     * Batch/Lot.
     */
    const val LOT = "1T"

    /**
     * Serial number.
     */
    const val SERIAL = "S"

    /**
     * Expiration date.
     */
    const val EXPIRATION = "D"

    /**
     * Manufacturing date.
     */
    const val MANUFACTURING = "16D"

}
